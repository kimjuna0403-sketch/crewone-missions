def main():
    """
    메인 실행 함수
    1. Supabase에서 전체 크루 목록 조회
    2. 오늘 미션이 없는 크루만 대상으로 GPT 호출
    3. 생성된 미션을 Supabase crew_daily_mission 테이블에 저장
    """
    today = date.today()
    print(f"📅 미션 생성 시작: {today} ({WEEKDAY_MAP[today.weekday()]})")
    print("=" * 60)

    # 전체 크루 조회
    crews = supabase.table("crews").select("*").execute().data
    print(f"🏃 전체 크루 수: {len(crews)}개")

    # 오늘 이미 생성된 미션 확인 (중복 방지)
    existing = supabase.table("crew_daily_mission")\
        .select("crew_id")\
        .eq("mission_date", str(today))\
        .execute().data
    existing_ids = {r["crew_id"] for r in existing}
    print(f"✅ 이미 생성된 미션: {len(existing_ids)}개")
    print(f"🎯 신규 생성 대상: {len(crews) - len(existing_ids)}개")
    print("=" * 60)

    success = 0
    fail = 0

    for crew in crews:
        # 오늘 미션이 이미 있으면 SKIP
        if crew["crew_id"] in existing_ids:
            print(f"  SKIP [{crew['crew_id']}] {crew['crew_name']}")
            continue

        try:
            # GPT로 오늘 미션 생성
            mission = generate_mission(crew, today)

            # Supabase에 저장
            supabase.table("crew_daily_mission").insert(mission).execute()

            print(f"  ✅ [{crew['crew_id']}] {crew['crew_name']}")
            print(f"     제목: {mission['mission_title']}")
            print(f"     설명: {mission['mission_desc']}")
            print(f"     인증: {mission['mission_type']}")
            success += 1

        except Exception as e:
            print(f"  ❌ [{crew['crew_id']}] {crew['crew_name']}: {e}")
            fail += 1

    print("=" * 60)
    print(f"🎉 완료! 성공: {success}개 | 실패: {fail}개")


if __name__ == "__main__":
    main()